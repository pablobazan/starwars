import 'package:starwars/home/domain/entities/invader.dart';

class InvaderModel extends Invader {
  const InvaderModel({
    required String name,
    required String height,
    required String mass,
    required String hairColor,
    required String skinColor,
    required String eyeColor,
    required String birthYear,
    required String gender,
    required String homeworld,
    required List<String> films,
    required List<dynamic> species,
    required List<String> vehicles,
    required List<String> starships,
    required DateTime created,
    required DateTime edited,
    required String url,
  }) : super(
            name: name,
            height: height,
            mass: mass,
            hairColor: hairColor,
            skinColor: skinColor,
            eyeColor: eyeColor,
            birthYear: birthYear,
            gender: gender,
            homeworld: homeworld,
            films: films,
            species: species,
            vehicles: vehicles,
            starships: starships,
            created: created,
            edited: edited,
            url: url);

  factory InvaderModel.fromJson(Map<String, dynamic> json) => InvaderModel(
        name: json['name'],
        height: json['height'],
        mass: json['mass'],
        hairColor: json['hair_color'],
        skinColor: json['sin_color'],
        eyeColor: json['eye_color'],
        birthYear: json['birth_year'],
        gender: json['gender'],
        homeworld: json['homeworld'],
        films: json['films'],
        species: json['species'],
        vehicles: json['vehicles'],
        starships: json['starships'],
        created: json['created'],
        edited: json['edited'],
        url: json['url'],
      );
}
